package Operators;

import java.util.* ;

//Unary Operators

public class U_OP {
    public static void main(String args[]){
    int a = 10 ;
    int b = ++a ;
    System.out.println(a);
    System.out.println(b);

    System.out.println("****************************");

    int c = 10 ;
    int d = a++ ;
    System.out.println(c);
    System.out.println(d);

    System.out.println("****************************");

    int e = 10 ;
    int f = --a ;
    System.out.println(e);
    System.out.println(f);

    System.out.println("****************************");

    int x = 10 ;
    int y = a-- ;
    System.out.println(x);
    System.out.println(y);

    System.out.println("****************************");

    }    
}
