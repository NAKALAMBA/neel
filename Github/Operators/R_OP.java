package Operators;

import java.util.*;

//Relation Operators 

public class R_OP {
    public static void main(String args[]){
        int A = 10;
        int B = 10 ;
        System.out.println(A == B ); // True
        System.out.println(A >= B ); // True
        System.out.println(A <= B ); // True
        System.out.println(A != B ); // false
        //System.out.println(A >= B );
    }
}
