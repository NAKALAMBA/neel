package Operators;




/*
 *      Assignment 2 operands
 *      = => 
 *      += =>
 *      -= =>
 *      *= =>
 *      /= =>
 */



public class Assignment {
    public static void main( String args[] ){
        int a = 10 ;
        a += 10 ;
        int b = 5 ;
        b -= 5 ;
        int c = 5 ;
        c *= 5 ;
        int d = 5 ;
        d /= 5 ;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
